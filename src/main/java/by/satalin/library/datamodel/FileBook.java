package by.satalin.library.datamodel;

/**
 * @author SATALIN
 *  child of entity book with FileName property
 */
public class FileBook extends Book {
	private String fileName;

	public FileBook() {

	}

	/**
	 * @return the fileName
	 */
	public String getFileName() {
		return fileName;
	}

	/**
	 * @param fileName
	 *            the fileName to set
	 */
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
}
